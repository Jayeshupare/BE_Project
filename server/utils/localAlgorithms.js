// 4. Plagiarism Checker: Enhanced Simulation (Style + Deterministic Probabilistic Matching)
export const checkLocalPlagiarism = (text) => {
    if (!text) return "Please provide text to check.";

    // 1. Cliché / Pattern Detection
    const commonPhrases = [
        "as a language model", "in conclusion", "it is important to note", "on the other hand", "all in all",
        "game changer", "cutting edge", "thought leader", "at the end of the day", "paradigm shift"
    ];
    let detected = [];
    commonPhrases.forEach(phrase => {
        if (text.toLowerCase().includes(phrase)) detected.push(phrase);
    });

    // 2. Lexical Diversity (Repetition Penalty)
    const words = text.toLowerCase().match(/\w+/g) || [];
    const uniqueWords = new Set(words);
    const lexicalDiversity = words.length > 0 ? (uniqueWords.size / words.length) * 100 : 100;

    // 3. Deterministic "Simulated Match" (Hash-based)
    // This ensures that the same text always gets the same score, but different texts get different scores.
    // We simulate "incidental matches" found on the web.
    let hash = 0;
    for (let i = 0; i < text.length; i++) {
        hash = ((hash << 5) - hash) + text.charCodeAt(i);
        hash |= 0; // Convert to 32bit integer
    }
    const simulatedMatchPercentage = Math.abs(hash % 20); // 0% to 19% simulated external matches

    // Calculate Score
    let score = 100;
    score -= (detected.length * 5); // Penalty for clichés
    if (lexicalDiversity < 40 && words.length > 20) score -= 15; // Penalty for repetition
    score -= simulatedMatchPercentage; // Penalty for simulated web matches

    if (score < 0) score = 0;

    return `
### Plagiarism & Originality Report
**Originality Score**: ${score.toFixed(1)}%
*(Higher is better. 100% = No matches found)*

**Analysis Breakdown**:
- **Cliché Usage**: ${detected.length > 0 ? `Detected (${detected.join(', ')})` : 'clean'}
- **Lexical Diversity**: ${lexicalDiversity.toFixed(1)}% ${lexicalDiversity < 40 ? '(Low - Repetitive)' : '(Good)'}
- **Web Similarity Index**: ~${simulatedMatchPercentage}% (Simulated trace matches)

*Note: This report is generated by a local algorithmic analysis of style, structure, and probabilistic matching patterns.*
    `.trim();
};

