// 2. Text Summarizer: Simple Extractive Summarization (Frequency-based)
export const summarizeLocalText = (text, length) => {
    if (!text) return "Please provide text to summarize.";

    const sentences = text.match(/[^.!?]+[.!?]+/g) || [text];
    if (sentences.length <= 3) return text; // Too short to summarize

    // Simple word frequency map
    const words = text.toLowerCase().match(/\w+/g) || [];
    const freq = {};
    words.forEach(w => freq[w] = (freq[w] || 0) + 1);

    // Score sentences
    const scores = sentences.map(s => {
        const sWords = s.toLowerCase().match(/\w+/g) || [];
        const score = sWords.reduce((acc, w) => acc + (freq[w] || 0), 0);
        return { text: s, score: score / (sWords.length || 1) }; // Normalize by length
    });

    // Sort by score
    scores.sort((a, b) => b.score - a.score);

    // Pick top N sentences based on length preference
    let count = length === 'Short' ? 2 : length === 'Medium' ? 4 : 6;
    return scores.slice(0, count).map(s => s.text).join(' ');
};

